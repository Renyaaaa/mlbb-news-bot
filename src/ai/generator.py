        response.raise_for_status()

        result = response.json()
        content = result["candidates"][0]["content"]["parts"][0]["text"].strip()
        print(f"‚úÖ Google Gemini —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–ª: {content}")
